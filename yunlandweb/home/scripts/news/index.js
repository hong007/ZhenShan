var createPagination =  function (pageobj,page_index, count,callback) {
    $(pageobj).pagination(count, {
        num_edge_entries: 3,
        num_display_entries: 4,
        items_per_page: 10,
        maxentries:count,
        next_text: '下一页',
        prev_text: '上一页',
        current_page: page_index,
        callback: callback
    });
}
